
public class bitwise_operators {
//bitwise opertaors
	public static void main(String[] args) {
		int a=4,b=7;
		System.out.println("a BITWISE AND b: " + (a & b));
		System.out.println("a BITWISE OR b: " + (a | b));
		System.out.println("a BITWISE XOE b: " + (a ^ b));
		System.out.println("a BITWISE NOT b: " + (~a));
		System.out.println("a BITWISE LEFT-SHIFT b: " + (a << b));
		System.out.println("a BITWISE RIGHT-SHIFT b: " + (a >> b));




	}

}
